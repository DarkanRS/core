package com.rs.lib.model;

public class DisplayNamePair {
	
	private String current;
	private String prev;
	
	public DisplayNamePair(String current, String prev) {
		this.current = current;
		this.prev = prev;
	}

	public String getCurrent() {
		return current;
	}

	public String getPrev() {
		return prev;
	}
	
	

}
